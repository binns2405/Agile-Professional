        <h1>What is Spear Phishing?</h1>
        <p>Phishing is a method of gathering information from a user and can be used to install malware on a victim's personal computer. Spear phishing is a highly targeted approach that is dangerous and difficult to prevent as it personally targets individuals.</p>
        
        <h2>Can be used to gather:</h2>
        <ul>
            <li>Bank details</li>
            <li>Personal details</li>
            <li>Home address</li>
            <li>Work data</li>
        </ul>
        
        <p>An attacker would examine the victim's social media to gather public information such as family members, friends, emails, recent locations, and purchases. They then use this information to disguise themselves as a trusted company or friend. This allows them to gain the victim’s trust and manipulate them into following malicious links that install malware or trick them into entering sensitive data.</p>
        
        <p>In 2019, 88% of organizations worldwide experienced a phishing attack, according to Proofpoint’s <em>State of the Phish</em> report. Of these, 55% suffered successful attacks.</p>
        
        <h2>How does it work?</h2>
        <ul>
            <li>Defining the goals of the attack</li>
            <li>Choosing the targets through preliminary research</li>
            <li>Identifying a shortlist of targets and researching them thoroughly</li>
            <li>Creating the spear phishing email using gathered information and social engineering techniques</li>
        </ul>
        
        <p>Spear phishing emails succeed because they appear familiar to the recipient. Attackers invest time and effort into learning as much as possible about their targets' jobs, personal lives, social circles, and families.</p>
        
        <h2>How to Prevent Phishing:</h2>
        <ul>
            <li>Check your inbox frequently for suspicious emails, such as password change requests or dubious links.</li>
            <li>Use a Virtual Private Network (VPN) for complete internet activity protection and encryption.</li>
            <li>Scan emails for potentially dangerous attachments, links, or downloads using an anti-virus program.</li>
            <li>Learn how to verify the accuracy of an email source.</li>
            <li>Validate websites and URLs to avoid clicking on dangerous links.</li>
            <li>Independently visit an organization’s official website instead of clicking links in emails.</li>
        </ul>
